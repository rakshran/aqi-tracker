// Helper function to get AQI category and color
export function getAQICategory(aqi) {
  if (aqi <= 50) return { label: 'Good', color: '#6BAB3E', textColor: 'text-green-700' };
  if (aqi <= 100) return { label: 'Moderate', color: '#F6B900', textColor: 'text-yellow-700' };
  if (aqi <= 150) return { label: 'Unhealthy for Sensitive Groups', color: '#E56E5A', textColor: 'text-orange-700' };
  if (aqi <= 200) return { label: 'Unhealthy', color: '#D73847', textColor: 'text-red-700' };
  if (aqi <= 300) return { label: 'Very Unhealthy', color: '#8B3A7A', textColor: 'text-purple-700' };
  return { label: 'Hazardous', color: '#7E0023', textColor: 'text-red-900' };
}
